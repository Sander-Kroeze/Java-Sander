package Les7_H12_Opdracht;

public class Opdracht {
	public static void main(String[] args) {

// 	int = cijfers
	int x = 35;
	int y = 46; 
//	geeft het hoogste getal weer van x en y
	System.out.println("Het grotere getal = " + Math.max(x, y));
	
//	double = cijfers met getallen achter de komma
	double z = 46.98;
	double s = 44.99;
//	geeft het laagste getal weer van z en s
	System.out.println("Het grotere getal = " + Math.min(z, s));
	
//	double = cijfers met getallen achter de komma
	double k = 81.0;
//	geeft de wortel van 81.0 weer
	System.out.println("Het grotere getal = " + Math.sqrt(k));
   }
}
